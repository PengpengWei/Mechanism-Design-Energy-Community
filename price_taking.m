function y = price_taking(q)
    y = zeros(6,1);
    
    y(1) = 1 / (-q(1) + q(7) + 0.1 + q(8)) - 2;
    y(2) = 2 / (-q(2) + q(7) + 0.2 + q(9)) - 2;
    
    y(3) = 2 / (-q(3) + q(7) + 0.1 + q(8)) - 2;
    y(4) = 4 / (-q(4) + q(7) + 0.2 + q(9)) - 2;
    
    y(5) = 3 / (-q(5) + q(7) + 0.1 + q(8)) - 2;
    y(6) = 6 / (-q(6) + q(7) + 0.2 + q(9)) - 2;
end